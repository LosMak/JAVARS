public class HW3_2 {
    public static void main(String[] args) {
        for (int i = 0; i < 16 ; i ++) {
            System.out.println(i);
            }
		System.out.println("_________________________");
/*Задача №2
Необходимо вывести все положительные степени числа 5
которые меньше 10000,
вывести результат возведения в степень.*/
		for (int i = 1; i < 10000; i = i * 5) {
			System.out.println(i);
        }
		System.out.println("_________________________");
/*Задача №3

Необходимо вывести все числа кратные 4 между числами 40 и 60 включительно.
Реализовать 2 варианта: 
использовать конструкцию if для определения кратности (цикл с шагом 1, i = i + 1);
без использования конструкции if (шаг цикла на ваше усмотрение).
*/    
		// Вариант 1: без использования if (шаг цикла 4)
		for (int i = 40; i <= 60; i = i + 4){
			System.out.println(i);
		}
		// Вариант 2: с использованием if (шаг цикла 1) - закомментирован для тестов
		for (int i = 40; i <= 60; i = i + 1) {
			if (i % 4 == 0) {
				System.out.println(i);
			}
		}
    }

}
